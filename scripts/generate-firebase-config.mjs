import fs from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const envPath = path.join(projectRoot, ".env");
const outPath = path.join(projectRoot, "public", "firebase-config.js");

function parseEnvFile(filePath) {
  const result = {};
  if (!fs.existsSync(filePath)) return result;
  const raw = fs.readFileSync(filePath, "utf8");
  const lines = raw.split(/\r?\n/);
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const idx = trimmed.indexOf("=");
    if (idx === -1) continue;
    const key = trimmed.slice(0, idx).trim();
    let value = trimmed.slice(idx + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    result[key] = value;
  }
  return result;
}

const fileEnv = parseEnvFile(envPath);
const keys = [
  "VITE_FIREBASE_API_KEY",
  "VITE_FIREBASE_AUTH_DOMAIN",
  "VITE_FIREBASE_PROJECT_ID",
  "VITE_FIREBASE_STORAGE_BUCKET",
  "VITE_FIREBASE_MESSAGING_SENDER_ID",
  "VITE_FIREBASE_APP_ID"
];

const cfg = {};
let hasAll = true;
for (const k of keys) {
  const v = process.env[k] ?? fileEnv[k];
  if (!v) hasAll = false;
  cfg[k] = v ?? "";
}

const vapidKey = process.env.VITE_FIREBASE_VAPID_KEY ?? fileEnv.VITE_FIREBASE_VAPID_KEY ?? "";
const hasVapid = !!vapidKey;

fs.mkdirSync(path.dirname(outPath), { recursive: true });

if (!hasAll || !hasVapid) {
  fs.writeFileSync(
    outPath,
    `// Generated by scripts/generate-firebase-config.mjs
// Set VITE_FIREBASE_* variables in your .env to enable push notifications.
self.__FIREBASE_CONFIG__ = null;
self.__FIREBASE_VAPID_KEY__ = ${JSON.stringify(vapidKey || "")};
`,
    "utf8"
  );
  // eslint-disable-next-line no-console
  console.warn(
    "[Semavik Tracker] Firebase env missing: push notifications disabled until you set .env"
  );
  process.exit(0);
}

const firebaseConfig = {
  apiKey: cfg.VITE_FIREBASE_API_KEY,
  authDomain: cfg.VITE_FIREBASE_AUTH_DOMAIN,
  projectId: cfg.VITE_FIREBASE_PROJECT_ID,
  storageBucket: cfg.VITE_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: cfg.VITE_FIREBASE_MESSAGING_SENDER_ID,
  appId: cfg.VITE_FIREBASE_APP_ID
};

fs.writeFileSync(
  outPath,
  `// Generated by scripts/generate-firebase-config.mjs
// DO NOT EDIT: generated from your .env
self.__FIREBASE_CONFIG__ = ${JSON.stringify(firebaseConfig)};
self.__FIREBASE_VAPID_KEY__ = ${JSON.stringify(vapidKey)};
`,
  "utf8"
);

